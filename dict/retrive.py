#Dictionary Keys, Values, and Items retrival

# Example dictionary
new_dict = {'name': 'momo', 'age': 28, 'city': 'Dhaka'}

# Retrieving keys, values, and items
keys = new_dict.keys()
values = new_dict.values()
items = new_dict.items()

# Printing keys, values, and items
print('the "keys"  in the dictionary are: {}'.format(keys))
print('the "values"  in the dictionary are: {}'.format(values))
print('the "items"  in the dictionary are: {}'.format(items))