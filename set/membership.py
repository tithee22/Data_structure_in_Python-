#Checking Membership in a Set


# Checking for membership by bulding a function
def check(se):
    if 3 in se:
        print("the value 3 is in the set ")
    else:
        print("the value 3 is not in the set ")

# Example set
my_set = {1, 2, 3}

# apply  function onto the example set
check(my_set)

