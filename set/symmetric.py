# Example 7: Set Symmetric Difference

# Example sets
set1 = {1, 2, 3}
set2 = {2, 3, 4}

# Performing symmetric difference from set1 to set2
symmetric_difference_set = set1.symmetric_difference(set2)
# Printing the symmetric difference set
print(' Symmetric Difference of the given Sets:', symmetric_difference_set)

# Performing symmetric difference from set2 to set1
symmetric_difference_set2 = set2.symmetric_difference(set1)
# Printing the symmetric difference set
print(' Symmetric Difference of the given Sets:', symmetric_difference_set2)
