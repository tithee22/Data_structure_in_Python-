# adding value into a set
my_set = {1, 2, 3}

# Adding a new element
my_set.add(6)
# Printing the set after adding a new value
print('After adding a new element, my set is:', my_set)

# Trying to add a duplicate element (this will have no effect)
my_set.add(2)
# Printing the set after attempting to add a duplicate value
print('After attempting to add a duplicate, my set is:', my_set)


