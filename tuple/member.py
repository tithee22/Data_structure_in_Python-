#Checking Membership in a Tuple

def check(new):
    if "apple" in new:
        print("'apple' is present in the tuple") 
    else:
        print("'apple' is not present in the tuple") 

# Example tuple
my_tuple = ('apple', 'banana', 'cherry', 'mango')
#apply the bulid function over the decleared tuple
check(my_tuple)
