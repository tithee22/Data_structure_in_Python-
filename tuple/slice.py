#Tuple Slicing


# Example tuple
new_tuple = (10, 20, 60, 30, 40, 50, 80)

# Slicing the tuple from index 1 to 4
sliced_tuple = new_tuple[1:4]
#slicing the tuple with negative indexing
nega_sli= new_tuple[-3:-1]

# Printing the sliced tuple
print('Sliced Tuple:', sliced_tuple)
print('the new negative scliced tuple is :{}'.format(nega_sli))